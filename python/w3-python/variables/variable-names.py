# NOTE: Rules for Python variables:

"""

1. A variable name must start with a letter or the underscore character
2. A variable name can not start with a number
3. A variable name can only contain alpha-numeric character and underscores (A-z, 0-9, and _)
4. Variable names are case-sensitive (age, Age and AGE are three different variables)
5. A variable name can not be any of the Python Keywords.

"""

# Ex:
# Legal variable names:
my_var = "John"
myVar = "John"

