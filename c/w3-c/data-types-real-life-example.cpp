#include<bits/stdc++.h>
using namespace std;

int main()
{
  // Here's a real-life example of using different data types, to calculate and output the total cost of a number of items:

  // Create variables of different data types
  int items = 50;
  double costPerItem = 9.99;
  double totalCost = items * costPerItem;
  char currency = '$';

  // Print variables
  cout << "Number of items: " << items << endl;
  cout << "Cost per item: " << costPerItem << "" << currency << endl;
  cout << "Total cost: " << totalCost << "" << currency << endl;

  return 0;
}